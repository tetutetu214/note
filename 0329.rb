　そういった行動が自分を変えていくのかどうか分からないけど、こういった行動を選択することを自分自身は望んでいる。

　変化というものを誰か持ち込んでくれると思って30年近く待っていたのだけれど、ようやく誰もが自分に必死で余裕がないことに、ようやく気づいたのです（おっそ。）

