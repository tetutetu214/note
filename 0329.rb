　ほんの少しだけ出来ればいいと思っていますが、いまも『誰か』を待ち続けている人に手を差し伸べられるような（人によっては傲慢に見えるでしょう）が、そんな人になるための手段としてエンジニアという道を選んだんだなと、思う心身ともに8.5割り程度満足の夜を過ごしながらポツンと夢を見るのでした。めでたしめでたし。

